// Define traducciones de funciones matemáticas en español.

#define absoluto abs        // Función para obtener el valor absoluto de un número (equivalente a abs)
#define raizCuadrada sqrt   // Función para calcular la raíz cuadrada de un número (equivalente a sqrt)
#define potencia pow        // Función para calcular la potencia de un número (equivalente a pow)
#define seno sin            // Función para calcular el seno de un ángulo (equivalente a sin)
#define coseno cos          // Función para calcular el coseno de un ángulo (equivalente a cos)
#define tangente tan        // Función para calcular la tangente de un ángulo (equivalente a tan)
#define arcoSeno asin       // Función para calcular el arco seno de un valor (equivalente a asin)
#define arcoCoseno acos     // Función para calcular el arco coseno de un valor (equivalente a acos)
#define arcoTangente atan   // Función para calcular el arco tangente de un valor (equivalente a atan)
#define arcoTangente2 atan2 // Función para calcular el arco tangente de dos valores (equivalente a atan2)
#define redondear round     // Función para redondear un número al entero más cercano (equivalente a round)
#define techo ceil          // Función para redondear hacia arriba un número al entero más cercano (equivalente a ceil)
#define piso floor          // Función para redondear hacia abajo un número al entero más cercano (equivalente a floor)

#define aleatoriaConSemilla randomSeed // Función para inicializar la generación de números aleatorios con una semilla (equivalente a randomSeed)
