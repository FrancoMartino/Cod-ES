// Define traducciones de funciones comunes de bits en español.

#define leerBits shiftIn     // Función de lectura de bits (equivalente a shiftIn)
#define enviarBits shiftOut  // Función de envío de bits (equivalente a shiftOut)
#define limpiarBit bitClear  // Función para limpiar un bit específico en un byte (equivalente a bitClear)
#define leerBit bitRead      // Función para leer el valor de un bit específico en un byte (equivalente a bitRead)
#define establecerBit bitSet // Función para establecer un bit específico en un byte (equivalente a bitSet)
#define escribirBit bitWrite // Función para escribir un valor en un bit específico en un byte (equivalente a bitWrite)
#define byteAlto highByte    // Función para obtener el byte alto de una palabra (equivalente a highByte)
#define byteBajo lowByte     // Función para obtener el byte bajo de una palabra (equivalente a lowByte)