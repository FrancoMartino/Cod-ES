// Define traducciones de constantes y modos de pin en español.

#define ALTO HIGH      // Constante que representa el nivel alto de un pin (equivalente a HIGH)
#define BAJO LOW       // Constante que representa el nivel bajo de un pin (equivalente a LOW)
#define ENCENDIDO HIGH // Constante que representa el estado de encendido (equivalente a HIGH)
#define APAGADO LOW    // Constante que representa el estado de apagado (equivalente a LOW)

#define CAMBIO CHANGE    // Modo de interrupción para cambios en el estado de un pin (equivalente a CHANGE)
#define ESCALANDO RISING // Modo de interrupción para flanco de subida (equivalente a RISING)
#define CAYENDO FALLING  // Modo de interrupción para flanco de bajada (equivalente a FALLING)

#define ENTRADA INPUT                   // Modo de configuración de un pin como entrada (equivalente a INPUT)
#define ENTRADA_POLARIZADA INPUT_PULLUP // Modo de entrada con resistencia pull-up activada (equivalente a INPUT_PULLUP)
#define SALIDA OUTPUT                   // Modo de configuración de un pin como salida (equivalente a OUTPUT)

#define LED_INTERNO LED_BUILTIN // Pin del LED interno en la placa (equivalente a LED_BUILTIN)

#define DIGITAL 0   // Modo de pin digital (equivalente a 0)
#define ANALOGICO 1 // Modo de pin analógico (equivalente a 1)